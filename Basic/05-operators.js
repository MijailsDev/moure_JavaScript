// OPERADORES

// Operadores aritmeticos 
let a = 5
let b = 10
console.log(a + b)     // Suma
console.log(a - b)     // Resta
console.log(a * b)     // Multiplicacion
console.log(a / b)     // Division

console.log(a % b)     // Modulo
console.log(a ** b)     // Exponentes

a++ // incremento
console.log(a)

b-- // Decremento
console.log(b)



